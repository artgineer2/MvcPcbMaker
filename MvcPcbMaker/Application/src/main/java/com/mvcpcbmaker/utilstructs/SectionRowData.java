package com.mvcpcbmaker.utilstructs;

public class SectionRowData {
	public String childPart;
	public String childPackage;
	public String parentPart;
	public String parentPackage;
	
	public SectionRowData(String childPart, String childPackage, String parentPart, String parentPackage)
	{
		this.childPart = childPart;
		this.childPackage = childPackage;
		this.parentPart = parentPart;
		this.parentPackage = parentPackage;
	}

}
